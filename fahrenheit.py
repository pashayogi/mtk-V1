#mengubah skala celsius menjadi fahrenheit
#dengan persamaan F = C X 9/5 + 32
#kunjungi Website Saya = http://hatakecnk.co.nf
celsius = float(input("Masukkan nilai suhu dalam skala derajat celsius : "))
fahrenheit = float(celsius * 9/5 + 32)
print("Nilai tersebut dalam skala fahrenheit sebesar : ", fahrenheit)